interface Player {
  id: string;
  ws: WebSocket;
  name: string;
  score: number;
  ready: boolean;
  difficulty: Difficulty;
  lastAnswer?: number;
  lastAnswerTime?: number;
}

interface Room {
  id: string;
  players: Player[];
  status: 'waiting' | 'starting' | 'playing' | 'finished';
  currentQuestionIndex: number;
  questions: QuestionData[];
  difficulty: Difficulty;
  timer: number;
  timerInterval?: NodeJS.Timeout;
}

const rooms = new Map<string, Room>();
let waitingPlayer: Player | null = null;

interface LeaderboardEntry {
  name: string;
  score: number;
}

let leaderboard: LeaderboardEntry[] = [];

function updateLeaderboard(newScores: LeaderboardEntry[]) {
  leaderboard = [...leaderboard, ...newScores]
    .sort((a, b) => b.score - a.score)
    .slice(0, 10);
}

function broadcast(room: Room, message: any) {
  const data = JSON.stringify(message);
  room.players.forEach(p => {
    if (p.ws.readyState === WebSocket.OPEN) {
      p.ws.send(data);
    }
  });
}

function getRandomQuestions(count: number) {
  const shuffled = [...FOOTBALL_QUESTIONS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

function startGame(room: Room) {
  room.status = 'playing';
  room.currentQuestionIndex = 0;
  room.questions = getRandomQuestions(QUESTIONS_COUNT);
  sendNextQuestion(room);
}

function sendNextQuestion(room: Room) {
  if (room.currentQuestionIndex >= QUESTIONS_COUNT) {
    finishGame(room);
    return;
  }

  room.timer = QUESTION_TIME;
  room.players.forEach(p => {
    p.lastAnswer = undefined;
    p.lastAnswerTime = undefined;
  });

  broadcast(room, {
    type: 'question',
    question: room.questions[room.currentQuestionIndex],
    index: room.currentQuestionIndex,
    total: QUESTIONS_COUNT,
    timer: room.timer
  });

  if (room.timerInterval) clearInterval(room.timerInterval);
  room.timerInterval = setInterval(() => {
    room.timer--;
    broadcast(room, { type: 'timer', value: room.timer });

    if (room.timer <= 0) {
      clearInterval(room.timerInterval);
      handleQuestionEnd(room);
    }
  }, 1000);
}

function handleQuestionEnd(room: Room) {
  const currentQ = room.questions[room.currentQuestionIndex];
  
  // Calculate scores for this round
  room.players.forEach(p => {
    if (p.lastAnswer === currentQ.answer) {
      p.score += 5;
    }
  });

  broadcast(room, {
    type: 'round_end',
    correctAnswer: currentQ.answer,
    scores: room.players.map(p => ({ id: p.id, score: p.score, lastAnswer: p.lastAnswer }))
  });

  setTimeout(() => {
    room.currentQuestionIndex++;
    sendNextQuestion(room);
  }, 3000);
}

function finishGame(room: Room) {
  room.status = 'finished';
  if (room.timerInterval) clearInterval(room.timerInterval);
  
  const finalScores = room.players.map(p => ({ id: p.id, name: p.name, score: p.score }));
  
  // Update global leaderboard
  updateLeaderboard(room.players.map(p => ({ name: p.name, score: p.score })));

  broadcast(room, {
    type: 'game_over',
    scores: finalScores,
    leaderboard
  });

  // Also broadcast updated leaderboard to everyone in lobby
  const leaderboardMsg = JSON.stringify({ type: 'leaderboard', leaderboard });
  wss.clients.forEach(client => {
    if (client.readyState === WebSocket.OPEN) {
      client.send(leaderboardMsg);
    }
  });
}

wss.on('connection', (ws) => {
  const playerId = nanoid();
  let currentPlayer: Player | null = null;
  let currentRoom: Room | null = null;

  ws.on('message', (data) => {
    const message = JSON.parse(data.toString());

    if (message.type === 'join') {
      currentPlayer = { id: playerId, ws, name: message.name || 'یاریزان', score: 0, ready: false, difficulty: 'easy' };
      
      // Send current leaderboard on join
      ws.send(JSON.stringify({ type: 'leaderboard', leaderboard }));

      if (message.mode === 'practice') {
        const roomId = nanoid();
        const room: Room = {
          id: roomId,
          players: [currentPlayer],
          status: 'starting',
          currentQuestionIndex: 0,
          questions: [],
          difficulty: 'easy',
          timer: QUESTION_TIME
        };
        rooms.set(roomId, room);
        currentRoom = room;
        
        ws.send(JSON.stringify({ type: 'init', id: playerId }));
        ws.send(JSON.stringify({
          type: 'match_found',
          roomId,
          players: room.players.map(p => ({ id: p.id, name: p.name }))
        }));

        setTimeout(() => startGame(room), 1500);
        return;
      }

      if (waitingPlayer) {
        const opponent = waitingPlayer;
        waitingPlayer = null;
        const roomId = nanoid();
        const room: Room = {
          id: roomId,
          players: [opponent, currentPlayer],
          status: 'starting',
          currentQuestionIndex: 0,
          questions: [],
          difficulty: 'easy',
          timer: QUESTION_TIME
        };
        rooms.set(roomId, room);
        currentRoom = room;
        
        // Send ID to the joining player
        ws.send(JSON.stringify({ type: 'init', id: playerId }));

        broadcast(room, {
          type: 'match_found',
          roomId,
          players: room.players.map(p => ({ id: p.id, name: p.name }))
        });

        setTimeout(() => startGame(room), 3000);
      } else {
        waitingPlayer = currentPlayer;
        ws.send(JSON.stringify({ type: 'init', id: playerId }));
        ws.send(JSON.stringify({ type: 'waiting' }));
      }
    }

    if (message.type === 'answer' && currentRoom && currentRoom.status === 'playing') {
      const player = currentRoom.players.find(p => p.id === playerId);
      if (player && player.lastAnswer === undefined) {
        player.lastAnswer = message.answer;
        player.lastAnswerTime = currentRoom.timer;
        
        // Notify other player that this player answered (but don't reveal what)
        broadcast(currentRoom, {
          type: 'player_answered',
          playerId: player.id
        });

        // If both answered, skip timer
        if (currentRoom.players.every(p => p.lastAnswer !== undefined)) {
          if (currentRoom.timerInterval) clearInterval(currentRoom.timerInterval);
          handleQuestionEnd(currentRoom);
        }
      }
    }
  });

  ws.on('close', () => {
    if (currentPlayer && waitingPlayer === currentPlayer) {
      waitingPlayer = null;
    }
    if (currentRoom) {
      broadcast(currentRoom, { type: 'opponent_disconnected' });
      rooms.delete(currentRoom.id);
    }
  });
});

async function start() {
  if (process.env.NODE_ENV !== "production") {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    app.use(express.static("dist"));
  }

  server.listen(PORT, "0.0.0.0", () => {
    console.log(`Server running on http://localhost:${PORT}`);
  });
}

start();
